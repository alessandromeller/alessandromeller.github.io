          <h4>Suave Caminho</h4>
          Assim… Ambos assim, no mesmo passo, <br>
          iremos percorrendo a mesma estrada; <br>
          tu – no meu braço trêmulo amparada, <br>
          eu – amparado no teu lindo braço. <br>
          <br>
          Ligados neste arrimo, embora escasso,  <br>
          venceremos as urzes da jornada. <br>
          E tu – te sentirás menos cansada, <br>
          e eu – menos sentirei o meu cansaço. <br>
          <br>
          E, assim, ligados pelos bens supremos,  <br>
          que para mim o teu carinho trouxe, <br>
          placidamente pela vida iremos, <br>
          <br>
          Calcando mágoas, afastando espinhos, <br>
          como se a escarpa desta vida fosse <br>
          o mais suave de todos os caminhos. <br>
          <br>
          - Mário Pederneiras

          <h4>Idílio</h4>
          <p>
            Quando nós vamos ambos, de mãos dadas, <br>
            Colher nos vales lírios e boninas, <br>
            E galgamos dum fôlego as colinas <br>
            Dos rocios da noite inda orvalhadas;<br>
            <br>
            Ou, vendo o mar, das ermas cumeadas <br>
            Contemplamos as nuvens vespertinas <br>
            Que parecem fantásticas ruínas <br>
            Ao longe, no horizonte, amontoadas: <br>
            <br>
            Quantas vezes, de súbito, emudeces! <br>
            Não sei que luz no teu olhar flutua; <br>
            Sinto tremer-te a mão, e empalideces... <br>
            <br>
            O vento e o mar murmuram orações, <br>
            E a poesia das cousas se insunua <br>
            Lenta e amorosa em nossos corações. <br>
            - Antero de Quental
          </p>

          <h4>Frustração</h4>
          <p>
            Foi bonito <br>
            O meu sonho de amor <br>
            Floriram em redor <br>
            Todos os campos em pousio <br>
            Um sol de Abril brilhou em pleno estio, <br>
            Lavado e promissor <br>
            Só que não houve frutos <br>
            Dessa primavera <br>
            A vida disse que era <br>
            Tarde demais <br>
            E que as paixões tardias <br>
            São ironias <br>
            Dos deuses desleais <br>
            - Miguel Torga, Diário XV

          </p>

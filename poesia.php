          <h4>Se Eu Conversasse Com Deus</h4>
          <p>
            Se eu conversasse com Deus <br>
            Iria lhe perguntar: <br>
            Por que é que sofremos tanto <br>
            Quando viemos pra cá? <br>
            Que dívida é essa <br>
            Que a gente tem que morrer pra pagar? <br>
            <br>
            Perguntaria também <br>
            Como é que ele é feito <br>
            Que não dorme, que não come <br>
            E assim vive satisfeito. <br>
            Por que foi que ele não fez <br>
            A gente do mesmo jeito? <br>
            <br>
            Por que existem uns felizes <br>
            E outros que sofrem tanto? <br>
            Nascemos do mesmo jeito, <br>
            Moramos no mesmo canto. <br>
            Quem foi temperar o choro <br>
            E acabou salgando o pranto? <br>
            - Leandro Gomes de Barros
          </p>

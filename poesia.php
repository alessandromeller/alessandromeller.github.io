          <h4>Chuva</h4>
          <p>
            Como se move a cama nestas <br>
            Noites de árvores que gesticulam <br>
            Quando pipoca forte a chuva <br>
            Brinquedo de lata, a chuva, com garbosos cascos <br>
            Trotando por sobre o teto sem fim <br>
            Viajando para o passado <br>
            <br>
            Por sobre velhos caminhos o galope da chuva <br>
            Escorrega, reduz a marcha, de novo acelera <br>
            Atravessando muitos, confusos anos; <br>
            Mas ele não consegue jamais dar o mergulho <br>
            No ponto mais profundo do passado <br>
            Porque o sol está lá. <br>
            - Vladimir Nabokov
          </p>

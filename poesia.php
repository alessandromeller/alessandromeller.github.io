          <h4>Al Monte Donde Fue Cartago</h4>
          <p>
            Excelso monte do el romano estrago <br>
            eterna mostrará vuestra memoria; <br>
            soberbios edificios do la gloria <br>
            aún resplandece de la gran Cartago; <br>
            <br>
            desierta playa, que apacible lago <br>
            lleno fuiste de triunfos y victoria; <br>
            despedazados mármoles, historia <br>
            en quien se ve cuál es del mundo el pago; <br>
            <br>
            arcos, anfiteatros, baños, templo, <br>
            que fuistes edificios celebrados <br>
            y agora apenas vemos las señales; <br>
            <br>
            gran remedio a mi mal es vuestro ejemplo: <br>
            que si del tiempo fuistes derribados, <br>
            el tiempo derribar podrá mis males. <br>
            <br>
            - Gutierre de Cetina
          </p>

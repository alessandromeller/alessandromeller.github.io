          <h4>Nenhuma dor</h4>
          Minha namorada tem segredos <br>
          Tem nos olhos mil brinquedos <br>
          De magoar o meu amor <br>
          Minha namorada muito amada <br>
          Não entende quase nada <br>
          Nunca vem de madrugada <br>
          Procurar por onde estou <br>
          É preciso, ó doce namorada <br>
          Seguirmos firmes na estrada <br>
          Que leva a nenhuma dor <br>
          Minha doce e triste namorada <br>
          Minha amada idolatrada <br>
          Salve-salve o nosso amor <br>
          <br>
          - Torquato Neto, em "Os últimos dias de paupérie".
